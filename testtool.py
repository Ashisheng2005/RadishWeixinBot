# ========== 彩虹渐变效果 ==========
rainbow = [91, 93, 92, 96, 94, 95]
text = "Hello, RadishWeixinBot!"
for i, ch in enumerate(text):
    print(f"\033[{rainbow[i % len(rainbow)]}m{ch}\033[0m", end="")
print()

print('\r' + ' ' * 30 + '\r', end='')
import time
for i in range(11):
    bar = "█" * i + "░" * (10 - i)
    print(f"\r\033[92m进度: [{bar}] {i*10}%\033[0m", end="")
    time.sleep(0.1)
print("\r", end='')  # 清除进度条

